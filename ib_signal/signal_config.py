from ib_signal.signal_modes import SignalWindowMode

# ============================================================
# РЕЖИМ ПОСТРОЕНИЯ СИГНАЛЬНЫХ ОКОН
# ============================================================

# Возможные значения:
# - SignalWindowMode.ROLLING — плавающее фиксированное окно от текущего бара;
# - SignalWindowMode.GRID    — сетка слотов, где анализ растёт от начала слота,
#                              вход разрешён только в первой части торгового участка,
#                              а закрытие сделки привязано к концу слота.
SIGNAL_WINDOW_MODE = SignalWindowMode.ROLLING

# Максимально допустимое отставание последнего job-бара от текущего времени.
# Если job DB отстала сильнее этого значения, signal-сервис считает её неготовой.
MAX_JOB_BAR_LAG_SECONDS = 15

# ============================================================
# ROLLING-РЕЖИМ
# ============================================================

# ROLLING работает от текущего закрытого бара:
#   back_start = current_bar - ROLLING_BACK_MINUTES
#   back_end   = current_bar
#   trade_from = current_bar
#   trade_to   = current_bar + ROLLING_TRADE_MINUTES

ROLLING_SIGNAL_STEP_SECONDS = 60  # как часто проверять необходимость расчёта в ROLLING
ROLLING_BACK_MINUTES = 30  # фиксированное окно анализа назад
ROLLING_TRADE_MINUTES = 30  # фиксированное окно прогноза/торговли вперёд

# ============================================================
# GRID-РЕЖИМ
# ============================================================

# GRID строит сигналы по сетке слотов.
#
# Внутри слота:
#   1. первые SLOT_BACK_MINUTES только копим анализ и не торгуем;
#   2. следующие SLOT_ENTRY_MINUTES можно искать сигнал и открывать сделку;
#   3. после этого новые входы запрещены, но уже открытая сделка может жить;
#   4. за SLOT_CLOSE_BEFORE_END_SECONDS секунд до конца слота сделка закрывается.

SLOT_SIGNAL_STEP_SECONDS = 5  # как часто проверять необходимость расчёта в слоте
SLOT_STEP_MINUTES = 60  # длина слота и шаг сетки стартов
SLOT_START_MINUTE_OF_DAY = 0  # якорь сетки от 00:00, в минутах
SLOT_BACK_MINUTES = 30  # сколько минут от начала слота только анализируем
SLOT_ENTRY_MINUTES = 20  # сколько минут после анализа разрешены новые входы
SLOT_CLOSE_BEFORE_END_SECONDS = 10  # за сколько секунд до конца слота закрываем сделку

# ============================================================
# ПОИСК КАНДИДАТОВ ПО PEARSON
# ============================================================

PRICE_SOURCE = "mid_close"  # источник цены для построения паттерна
PEARSON_MIN = 0.7  # минимальная корреляция кандидата
MIN_CANDIDATES = 10  # минимум кандидатов для пригодного сигнала
MAX_CANDIDATES = 100  # максимум лучших кандидатов после сортировки
CANDIDATE_SEARCH_STEP_SECONDS = 60  # шаг перебора исторических окон
HISTORY_LOOKBACK_DAYS = 120  # глубина истории для поиска кандидатов
