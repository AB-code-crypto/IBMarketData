# Возможные значения:
# - "ROLLING" — плавающее фиксированное окно от текущего бара;
# - "GRID"    — сетка слотов, где анализ растёт от начала слота,
#               вход разрешён только в первой части торгового участка,
#               а закрытие сделки привязано к концу слота.
SIGNAL_WINDOW_MODE = "ROLLING"

# Общий запас по времени, чтобы не использовать последний ещё неустойчивый бар.
LAST_BAR_SAFETY_SECONDS = 10

# ============================================================
# ROLLING-РЕЖИМ
# ============================================================

# ROLLING работает от текущего закрытого бара:
#   back_start = current_bar - ROLLING_BACK_MINUTES
#   back_end   = current_bar
#   trade_from = current_bar
#   trade_to   = current_bar + ROLLING_TRADE_MINUTES
#
# Пример:
#   ROLLING_BACK_MINUTES = 30, ROLLING_TRADE_MINUTES = 30
#   10:18 -> анализ 09:48-10:18, торговое окно 10:18-10:48
#   10:19 -> анализ 09:49-10:19, торговое окно 10:19-10:49

ROLLING_SIGNAL_STEP_SECONDS = 60  # как часто считать новый сигнал в ROLLING
ROLLING_BACK_MINUTES = 30  # фиксированное окно анализа назад
ROLLING_TRADE_MINUTES = 30  # фиксированное окно прогноза/торговли вперёд

# ============================================================
# GRID-РЕЖИМ
# ============================================================

# GRID строит сигналы по сетке слотов.
#
# Слот начинается только на заданной сетке SLOT_STEP_MINUTES,
# начиная от якоря SLOT_START_MINUTE_OF_DAY.
#
# Внутри слота:
#   1. первые SLOT_BACK_MINUTES только копим анализ и не торгуем;
#   2. следующие SLOT_ENTRY_MINUTES можно искать сигнал и открывать сделку;
#   3. после этого новые входы запрещены, но уже открытая сделка может жить;
#   4. за SLOT_CLOSE_BEFORE_END_SECONDS секунд до конца слота сделка закрывается.
#
# Пример:
#   SLOT_STEP_MINUTES = 60
#   SLOT_BACK_MINUTES = 30
#   SLOT_ENTRY_MINUTES = 20
#   SLOT_CLOSE_BEFORE_END_SECONDS = 10
#
#   10:00-10:30 -> анализ копится, вход запрещён
#   10:30-10:50 -> можно считать сигнал и открывать сделку
#   10:50-10:59:50 -> новые входы запрещены, открытая сделка может жить
#   10:59:50 -> закрываем открытую сделку
#
# Если SLOT_STEP_MINUTES = 30, слоты стартуют в 10:00, 10:30, 11:00, 11:30...

SLOT_SIGNAL_STEP_SECONDS = 5  # как часто считать новый сигнал в слоте
SLOT_STEP_MINUTES = 60  # длина слота и шаг сетки стартов
SLOT_START_MINUTE_OF_DAY = 0  # якорь сетки от 00:00, в минутах
SLOT_BACK_MINUTES = 30  # сколько минут от начала слота только анализируем
SLOT_ENTRY_MINUTES = 20  # сколько минут после анализа разрешены новые входы
SLOT_CLOSE_BEFORE_END_SECONDS = 10  # за сколько секунд до конца слота закрываем сделку

