# Возможные значения:
# - "ROLLING" — плавающее фиксированное окно от текущего бара;
# - "GRID"    — сетка слотов, где анализ растёт от начала слота,
#               а торговое окно уменьшается до конца торговой части слота.
SIGNAL_WINDOW_MODE = "ROLLING"

# Общий запас по времени, чтобы не использовать последний ещё неустойчивый бар.
LAST_BAR_SAFETY_SECONDS = 10


# ============================================================
# ROLLING-РЕЖИМ
# ============================================================

# ROLLING работает от текущего закрытого бара:
#   back_start = current_bar - BACK_WINDOW_MINUTES
#   back_end   = current_bar
#   trade_from = current_bar
#   trade_to   = current_bar + TRADE_WINDOW_MINUTES
#
# Пример:
#   BACK_WINDOW_MINUTES = 30, TRADE_WINDOW_MINUTES = 30
#   10:18 -> анализ 09:48-10:18, торговое окно 10:18-10:48
#   10:19 -> анализ 09:49-10:19, торговое окно 10:19-10:49

ROLLING_SIGNAL_STEP_SECONDS = 60  # как часто считать новый сигнал в ROLLING
BACK_WINDOW_MINUTES = 30          # фиксированное окно анализа назад
TRADE_WINDOW_MINUTES = 30         # фиксированное окно прогноза/торговли вперёд


# ============================================================
# GRID-РЕЖИМ
# ============================================================

# GRID строит сигналы по сетке слотов.
#
# Слот начинается только на заданной сетке GRID_SLOT_STEP_MINUTES.
# Внутри слота сначала копится минимальное окно анализа, затем начинается
# торговая часть. Во время торговой части:
#   - окно анализа растёт от начала слота до текущего бара;
#   - торговое окно уменьшается от текущего бара до конца торговой части;
#   - новые входы запрещаются за GRID_CLOSE_BEFORE_TRADE_END_SECONDS секунд
#     до конца торговой части.
#
# Пример режима внутри часа:
#   GRID_SLOT_STEP_MINUTES = 60
#   GRID_MIN_BACK_WINDOW_MINUTES = 30
#   GRID_TRADE_WINDOW_MINUTES = 30
#   10:00-10:30 -> анализ копится, сигнал не считаем
#   10:30       -> анализ 10:00-10:30, торговля до 10:59:50
#   10:45       -> анализ 10:00-10:45, торговля до 10:59:50
#
# Пример режима каждые 30 минут:
#   GRID_SLOT_STEP_MINUTES = 30
#   слоты стартуют в 10:00, 10:30, 11:00, 11:30...

GRID_SIGNAL_STEP_SECONDS = 5               # как часто считать новый сигнал в GRID
GRID_SLOT_STEP_MINUTES = 60                # длина слота в минутах
GRID_ANCHOR_MINUTE_OF_DAY = 0              # якорь сетки от 00:00, в минутах
GRID_MIN_BACK_WINDOW_MINUTES = 30          # минимальное окно анализа до торговли
GRID_TRADE_WINDOW_MINUTES = 30             # максимальная торговая часть слота
GRID_ENTRY_STOP_BEFORE_TRADE_END_SECONDS = 600  # запрет новых входов до конца торговли
GRID_CLOSE_BEFORE_TRADE_END_SECONDS = 10   # закрытие сделки до конца торговли


