"""
============================================================
РЕЖИМ ПОСТРОЕНИЯ СИГНАЛЬНЫХ ОКОН
============================================================

"ROLLING"
    Обычный плавающий режим.
    На каждом расчёте берём фиксированное окно назад от текущего бара
    и фиксированное окно вперёд для оценки будущего движения.

    Пример при BACK_WINDOW_MINUTES=30 и TRADE_WINDOW_MINUTES=30:
        10:18 -> анализ 09:48-10:18, торговое окно 10:18-10:48
        10:19 -> анализ 09:49-10:19, торговое окно 10:19-10:49

"GRID"
    Режим сетки слотов.
    Слот — это заранее размеченный временной участок:
        начало слота -> накопление анализа -> торговая часть слота.

    Внутри торговой части слот ведёт себя так:
        - окно анализа растёт от начала слота до текущего бара;
        - оставшееся торговое окно уменьшается до конца торговой части;
        - сделка должна быть закрыта за GRID_CLOSE_BEFORE_TRADE_END_SECONDS
          секунд до конца торговой части.

    Пример режима "внутри часа":
        GRID_SLOT_STEP_MINUTES = 60
        GRID_MIN_BACK_WINDOW_MINUTES = 30
        GRID_TRADE_WINDOW_MINUTES = 30

        10:00-10:30 -> только накопление анализа, сигнал не считаем
        10:30       -> анализ 10:00-10:30, торговля до 10:59:50
        10:45       -> анализ 10:00-10:45, торговля до 10:59:50
        10:59:50    -> конец торговой части, новые входы запрещены

    Если GRID_SLOT_STEP_MINUTES = 30, новый слот начинается каждые 30 минут.
    Тогда торговые части тоже появляются каждые 30 минут, но каждый слот всё
    равно имеет своё начало, своё растущее окно анализа и свой конец торговли.
"""
SIGNAL_WINDOW_MODE = "ROLLING"

# ============================================================
# ОБЩИЕ НАСТРОЙКИ РАСЧЁТА СИГНАЛА
# ============================================================

# Как часто IBSignal принимает новое решение.
# 60 секунд означает: один расчёт в начале каждой минуты.
# Для GRID-режима можно поставить 5 секунд, если надо двигаться по каждому бару.
SIGNAL_STEP_SECONDS = 60

# Небольшой запас по времени, чтобы не использовать последние ещё неустойчивые бары.
# Например, если БД пишется 5-секундными барами, то 5-10 секунд запаса помогают
# не попасть в ситуацию, когда последний бар ещё не дописан.
LAST_BAR_SAFETY_SECONDS = 10

# ============================================================
# ROLLING-РЕЖИМ
# ============================================================

# Сколько минут берём назад от момента принятия решения для поиска похожего паттерна.
# Используется только при SIGNAL_WINDOW_MODE = "ROLLING".
BACK_WINDOW_MINUTES = 30

# На сколько минут вперёд смотрим после найденного исторического кандидата.
# Это же окно в будущем будет использоваться торговым ботом как базовая
# длительность сделки/сигнала.
# Используется при SIGNAL_WINDOW_MODE = "ROLLING".
TRADE_WINDOW_MINUTES = 30

# ============================================================
# GRID-РЕЖИМ: СЕТКА СЛОТОВ
# ============================================================

# Шаг сетки стартов слота.
#
# Это одновременно:
# - дискретность, на которой могут начинаться слоты;
# - расстояние между началами соседних слотов.
#
# GRID_SLOT_STEP_MINUTES = 60:
#     слоты начинаются раз в час: 10:00, 11:00, 12:00...
#
# GRID_SLOT_STEP_MINUTES = 30:
#     слоты начинаются каждые 30 минут: 10:00, 10:30, 11:00, 11:30...
#
# GRID_SLOT_STEP_MINUTES = 120:
#     слоты начинаются раз в два часа: 10:00, 12:00, 14:00...
#
# Важно: именно этот параметр не даёт получить случайный старт слота
# вроде 10:14 или 10:37, если такая дискретность не задана явно.
GRID_SLOT_STEP_MINUTES = 60

# Якорь сетки внутри суток, в минутах от 00:00.
#
# 0 означает, что сетка стартует от начала суток:
#     00:00, 00:30, 01:00, 01:30...
#
# Например, если позже понадобится привязка к 09:30, можно будет поставить:
#     GRID_ANCHOR_MINUTE_OF_DAY = 9 * 60 + 30
#
# Пока оставляем 0, чтобы сетка была ровной и простой.
GRID_ANCHOR_MINUTE_OF_DAY = 0

# Минимальная длина анализа до начала торговой части слота.
#
# Пример:
#     GRID_MIN_BACK_WINDOW_MINUTES = 30
#
# Тогда первые 30 минут слота IBSignal только копит окно анализа.
# Сигнал начинает рассчитываться только после того, как с начала слота
# прошло минимум 30 минут.
GRID_MIN_BACK_WINDOW_MINUTES = 30

# Максимальная длительность торговой части слота.
#
# Полная длительность слота логически равна:
#     GRID_MIN_BACK_WINDOW_MINUTES + GRID_TRADE_WINDOW_MINUTES
#
# Примеры:
#     30 + 30 = режим внутри часа;
#     90 + 30 = режим внутри двух часов;
#     47 + 9  = длинный анализ и короткая торговля.
GRID_TRADE_WINDOW_MINUTES = 30

# За сколько секунд до конца торговой части слота нужно закрыть сделку
# и перестать открывать новые входы.
#
# Пример для режима внутри часа:
#     торговая часть заканчивается в 11:00:00;
#     GRID_CLOSE_BEFORE_TRADE_END_SECONDS = 10;
#     значит фактический край торговли — 10:59:50.
GRID_CLOSE_BEFORE_TRADE_END_SECONDS = 10

# ============================================================
# ПОИСК КАНДИДАТОВ ПО PEARSON
# ============================================================

# Источник цены для построения паттерна.
# На первом этапе используем mid close:
#   mid_close = (bid_close + ask_close) / 2
PRICE_SOURCE = "mid_close"

# Минимально допустимое значение Pearson correlation.
# Кандидаты ниже этого порога не участвуют в прогнозе.
PEARSON_MIN = 0.7

# Минимальное количество кандидатов, необходимое для того,
# чтобы сигнал можно было считать статистически пригодным.
MIN_CANDIDATES = 10

# Максимальное количество лучших кандидатов, которое оставляем после сортировки.
MAX_CANDIDATES = 100

# Шаг скользящего окна при переборе исторических кандидатов.
# 5 секунд = максимально плотный поиск по исходным 5-секундным барам.
# 60 секунд = быстрее и меньше почти дублирующихся кандидатов.
#
# Для GRID-режима позже можно будет добавить отдельный режим выравнивания
# кандидатов по слотам, чтобы исторические кандидаты имели такую же фазу
# внутри слота, как и текущий сигнал.
CANDIDATE_SEARCH_STEP_SECONDS = 60

# Глубина истории, по которой ищем похожие паттерны.
# Позже этот параметр можно будет оптимизировать отдельно по инструментам.
HISTORY_LOOKBACK_DAYS = 120
