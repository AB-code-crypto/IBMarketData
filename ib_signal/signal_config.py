from ib_signal.signal_modes import SignalWindowMode

# ============================================================
# РЕЖИМ ПОСТРОЕНИЯ СИГНАЛЬНЫХ ОКОН
# ============================================================

# Возможные значения:
# - SignalWindowMode.ROLLING — плавающее фиксированное окно от последнего
#                              доступного закрытого job-бара;
# - SignalWindowMode.GRID    — сетка слотов, где сначала накапливается
#                              анализируемый участок, а затем signal-сервис
#                              может пересчитывать сигнал по сетке времени.
SIGNAL_WINDOW_MODE = SignalWindowMode.ROLLING

# Максимально допустимое отставание последнего job-бара от текущего времени.
# Если job DB отстала сильнее этого значения, signal-сервис считает её неготовой.
MAX_JOB_BAR_LAG_SECONDS = 15

# ============================================================
# ROLLING-РЕЖИМ
# ============================================================

# ROLLING работает от последнего доступного закрытого job-бара.
#
# Важно:
#   current_bar в формулах ниже — это не формирующийся live-бар,
#   а последний bar_time_ts, который уже записан в job DB.
#
#   signal_bar = последняя расчётная точка ROLLING, покрытая job DB
#   back_start = signal_bar - ROLLING_BACK_MINUTES
#   back_end   = signal_bar
#   trade_from = signal_bar
#   trade_to   = signal_bar + ROLLING_TRADE_MINUTES

ROLLING_SIGNAL_STEP_SECONDS = 60  # как часто проверять необходимость расчёта в ROLLING
ROLLING_BACK_MINUTES = 30  # фиксированное окно анализа назад
ROLLING_TRADE_MINUTES = 30  # фиксированное окно прогноза/торговли вперёд

# ============================================================
# GRID-РЕЖИМ
# ============================================================

# GRID строит сигналы по сетке слотов.
#
# Внутри слота:
#   1. первые SLOT_BACK_MINUTES только копим анализируемый участок;
#   2. после SLOT_BACK_MINUTES signal-сервис может пересчитывать сигнал
#      каждые SLOT_SIGNAL_STEP_SECONDS;
#   3. SLOT_ENTRY_MINUTES описывает окно первичного входа для будущей логики
#      исполнения, но не запрещает самому ib_signal продолжать расчёт сигналов;
#   4. дальнейшее сопровождение позиции, закрытие, переворот или игнорирование
#      противоположного сигнала решает отдельный контур контроля позиции;
#   5. за SLOT_CLOSE_BEFORE_END_SECONDS секунд до конца слота новые расчёты
#      сигнала прекращаются.

SLOT_SIGNAL_STEP_SECONDS = 5  # как часто проверять необходимость расчёта в слоте
SLOT_STEP_MINUTES = 60  # длина слота и шаг сетки стартов
SLOT_START_MINUTE_OF_DAY = 0  # якорь сетки от 00:00, в минутах
SLOT_BACK_MINUTES = 30  # сколько минут от начала слота только анализируем
SLOT_ENTRY_MINUTES = 20  # окно первичного входа после анализа, в минутах
SLOT_CLOSE_BEFORE_END_SECONDS = 10  # за сколько секунд до конца слота прекращаем расчёт

